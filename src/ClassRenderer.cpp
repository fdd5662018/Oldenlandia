#include "ClassRenderer.h"

// Render data
GLuint ClassRenderer::m_cubeVAO;
GLuint ClassRenderer::m_NDCQuadVAO;

void ClassRenderer::Render(Shader &shader, RENDERTYPE type)
{
	switch (type) 
	{
		case CUBE:	RenderCube(shader); break;
		case NDCQUAD:	RenderNDCQuad(shader); break;
		default:	printf("ClassRenderer::Type error: %d\n", type);
	}
}

void ClassRenderer::RenderCube(Shader &shader) {
	// initialize (if necessary)
	if (m_cubeVAO == 0)
	{
		initCubeData();
	}
	// render Cube
	shader.use();
	glBindVertexArray(m_cubeVAO);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

void ClassRenderer::RenderNDCQuad(Shader &shader) {
	if (m_NDCQuadVAO == 0)
	{
		initNDCQuaddata();
	}
	// render NDCQuad
	shader.use();
	glBindVertexArray(m_NDCQuadVAO);
	glDrawArrays(GL_TRIANGLE_STRIP, 0, 4);
	glBindVertexArray(0);
}

